    @staticmethod
    def create_icon(svg_string, color="#111827", size=24):
        """
        创建QIcon从SVG字符串
        color: 图标颜色（十六进制）
        size: 图标大小（像素）
        """
        from PyQt5.QtSvg import QSvgRenderer
        from PyQt5.QtGui import QPixmap, QPainter, QIcon, QColor
        from PyQt5.QtCore import QByteArray, Qt

        # 替换颜色
        svg_colored = svg_string.replace('stroke="currentColor"', f'stroke="{color}"')

        # 创建渲染器
        renderer = QSvgRenderer(QByteArray(svg_colored.encode()))

        # 创建pixmap
        pixmap = QPixmap(size, size)
        pixmap.fill(Qt.transparent)  # 透明背景

        # 绘制SVG
        painter = QPainter(pixmap)
        renderer.render(painter)
        painter.end()

        return QIcon(pixmap)
